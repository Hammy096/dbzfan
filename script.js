// Display random characters
function displayRandomCharacters(characters) {
    const characterList = document.getElementById('characterList');
    characterList.innerHTML = '';

    const randomCharacters = getRandomItems(characters, 5);

    randomCharacters.forEach(character => {
        characterList.appendChild(createCharacterCard(character));
    });
}

// Display random characters with max DP of 15
function displayRandomCharactersWithDP(characters, maxDP) {
    const characterList = document.getElementById('characterList');
    characterList.innerHTML = '';

    let totalDP = 0;
    const selectedCharacters = [];

    while (totalDP < maxDP) {
        const randomCharacter = characters[Math.floor(Math.random() * characters.length)];
        if (!selectedCharacters.includes(randomCharacter) && totalDP + randomCharacter.dp <= maxDP) {
            selectedCharacters.push(randomCharacter);
            totalDP += randomCharacter.dp;
        }
    }

    selectedCharacters.forEach(character => {
        characterList.appendChild(createCharacterCard(character));
    });
}

// Get the reset button and the character list container
const resetButton = document.getElementById('resetButton');
const characterList = document.getElementById('characterList');

resetButton.addEventListener('click', () => {
    // Add a fade-out effect before clearing
    characterList.classList.add('hidden');
    setTimeout(() => {
        characterList.innerHTML = ''; // Clear content after animation
        characterList.classList.remove('hidden'); // Reset for future generations
    }, 500); // Match timeout with the CSS transition duration
});




// Helper functions
function getRandomItems(array, count) {
    const shuffled = [...array].sort(() => 0.5 - Math.random());
    return shuffled.slice(0, count);
}

function createCharacterCard(character) {
    const li = document.createElement('li');
    li.className = 'flex flex-col items-center p-4 bg-white rounded shadow-md';

    const img = document.createElement('img');
    img.src = character.Image;
    img.alt = character.name;
    img.className = 'w-24 h-24 rounded-full mb-2';

    const span = document.createElement('span');
    span.textContent = `${character.name} (DP: ${character.dp})`;
    span.className = 'text-lg font-semibold';

    li.appendChild(img);
    li.appendChild(span);

    return li;
}